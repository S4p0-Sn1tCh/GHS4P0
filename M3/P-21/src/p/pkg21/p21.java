    private boolean PrimoFunct(int numbr) {
        int cntr = 2;
        boolean primo = true;
        while ((primo) && (cntr != numbr)) {
            if (numbr % cntr == 0) {
                primo = false;
            }
            cntr++;
        }
        return primo;
    }

