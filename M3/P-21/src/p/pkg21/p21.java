    private int FunctionDiasViv(int dia, int mes, int anyo) throws ParseException {
        int value = 0;
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        Date date1 = sdf.parse(dia + "/" + mes + "/" + anyo);
        Date date2 = new Date();
        long diff = date2.getTime() - date1.getTime();

        value = (int) TimeUnit.DAYS.convert(diff, TimeUnit.MILLISECONDS);

        return value;

    }

