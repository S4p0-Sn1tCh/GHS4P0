    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String diaStrg;
        dia = Integer.parseInt(jComboBoxDia.getSelectedItem().toString());
        mes = Integer.parseInt(jComboBoxMes.getSelectedItem().toString());
        anyo = Integer.parseInt(jComboBoxAnyo.getSelectedItem().toString());
        try {
            int day = FunctionDiasViv(dia, mes, anyo);
            jTextFieldDiasViv.setText(String.valueOf(day));
        } catch (ParseException ex) {
            System.out.println("Error");
        }

        diaStrg = functionDiaNacimiento(dia, mes, anyo);
        jTextFieldDiaNacimiento.setText(diaStrg);

        bisest = bisiest(anyo);
        if (bisest == 1) {
            jCheckBoxBisiesto.setSelected(true);
        } else {
            jCheckBoxBisiesto.setSelected(false);
        }

        int numbr = dia + mes + anyo;

        boolean Prim = PrimoFunct(numbr);
        boolean Alegre = fechaAlegre(numbr);
        boolean Especial = fechaEspecial(numbr);

        if (Prim == true) {
            jRadioButtonDiaMagico.setSelected(true);
            jRadioButtonFechaAlegre.setSelected(false);
            jRadioButtonFechaEspecial.setSelected(false);
        } else {
            jRadioButtonDiaMagico.setSelected(false);
            jRadioButtonFechaEspecial.setSelected(Especial);
            jRadioButtonFechaAlegre.setSelected(Alegre);
        }

        int diaMes;
        diaMes = FunctionDiaMes(mes, anyo);
        jTextFieldDiasMes.setText(diaMes + " Dias Totales");

        String NomMes = FunctionNomMes(mes);
        jTextFieldNomMes.setText(NomMes);
        
        String Dias5k = FunctionDDias5000(dia, mes, anyo);
        jTextField5kDias.setText(Dias5k);
        
        String Dias10k = FunctionDDias10000(dia, mes, anyo);
        jTextField10kDias.setText(Dias10k);
        
        String Dias20k = FunctionDDias20000(dia, mes, anyo);
        jTextField20kDias.setText(Dias20k);
        
        
        


    }//GEN-LAST:event_jButton1ActionPerformed

