    private String functionDiaNacimiento(int dia, int mes, int anyo) {
        //Zeller
        String diaString = "";
        String[] semana = {"Domingo", "Lunes", "Martes", "Miercoles", "Viernes", "Sabado"};

        int a, y, m, d;
        dia = 28;
        a = (14 - mes) / 12;
        y = anyo - a;
        m = mes + 12 * a - 2;
        d = (dia + y + y / 4 - y / 100 + y / 400 + (31 * m) / 12) % 7;
        diaString = semana[d];

        return diaString;

    }

